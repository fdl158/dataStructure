package cn.delingw.LeetCode.LC206;

/**
 * @author delingw
 * @version 1.0
 * 反转链表
 */
public class ListNode {
    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}
